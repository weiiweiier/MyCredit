CREATE TABLE billrecord (
	UUID				VARCHAR(36),	--交易編號UUID
    BUY_CHANNEL 		VARCHAR(2) ,	--交易通路(01:虛擬/02:實體)
	BUY_DATE 			VARCHAR(23),	--消費時間yyyy/MM/dd HH:mm:ss.SSS
	REQ_PAYMENT_DATE	VARCHAR(23),	--請款時間yyyy/MM/dd HH:mm:ss.SSS
	SHOP_ID				VARCHAR(20),	--消費店家(統編)
	CUSTOMER_ID			VARCHAR(10),	--消費者(身分證)
	BUY_CURRENCY		VARCHAR(10),	--消費幣別
	BUY_AMOUNT			VARCHAR(10),	--消費金額
	DISPUTED_FLAG		VARCHAR(2) ,	--爭議款項註記(00:正常,01異常)
	STATUS				VARCHAR(2) ,	--狀態(00:正常,99:註銷)
	ACTUALLY_DATE		VARCHAR(23),	--交易完成的時間yyyy/MM/dd HH:mm:ss.SSS
	REMARK				VARCHAR(50),	--備註
	ISSUING_BANK		VARCHAR(50),	--發卡銀行(swiftCode)
	CARD_NUM			VARCHAR(20),	--卡號
	SECURITY_CODE		VARCHAR(10) 	--安全碼
 )

